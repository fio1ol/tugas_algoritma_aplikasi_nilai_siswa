        "        return\n",
        "    print(\"\\nDaftar Nilai Siswa:\")\n",
        "    for i, siswa in enumerate(data_siswa, 1):\n",
        "        print(f\"{i}. {siswa['nama']} - {siswa['nilai']}\")\n",
        "    print()\n",
        "\n",
        "# Fungsi untuk menghitung rata-rata nilai\n",
