        "# Membuat program CLI untuk Manajemen Nilai Siswa\n",
        "# Fitur: tambah data siswa, lihat data, hitung rata-rata, cari nilai tertinggi/terendah, simpan ke file\n",
        "\n",
        "import json\n",
        "\n",
        "# Data disimpan dalam bentuk list of dict\n",
        "data_siswa = []\n",
        "\n",
        "# Fungsi untuk menambahkan siswa\n",
        "def tambah_siswa():\n",
        "    nama = input(\"Masukkan nama siswa: \")\n",
        "    try:\n",
        "        nilai = float(input(\"Masukkan nilai siswa: \"))\n",
        "        data_siswa.append({\"nama\": nama, \"nilai\": nilai})\n",
        "        print(f\"Siswa {nama} dengan nilai {nilai} berhasil ditambahkan!\\n\")\n",
        "    except ValueError:\n",
        "        print(\"Nilai harus berupa angka!\\n\")\n",
        "\n",
        "# Fungsi untuk menampilkan semua data siswa\n",
        "def tampilkan_data():\n",
        "    if not data_siswa:\n",
